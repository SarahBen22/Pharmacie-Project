<p>Créée par Samuel Hahnemann au début du XIXe siècle, l'homéopathie appartient au domaine des médecines douces.
Elle est pratiquée par les médecins dans l'ensemble du monde. Le terme "homéopathie" provient des mots grecs "homoios",
 se traduisant par "similaire" et "pathos" signifiant "maladie". En effet, selon l'homéopathie,
 le semblable guérit le semblable.

Le principe de l'homéopathie repose sur l'idée que le corps est capable de s'auto-guérir. Ainsi,
les substances utilisées en homéopathie visent à reproduire les symptômes provoqués par la maladie à soigner,
 afin de renforcer les capacités de guérison du patient. Par ailleurs,
  chaque personne est prise en compte dans son individualité et sa globalité afin de trouver les traitements
  les plus adaptés.
  Les produits homéopathiques sont hautement dilués, car cette médecine douce considère que cela permet de
  renforcer leurs effets curatifs. Les substances de base proviennent du monde animal, végétal et minéral.


